profile6_face_builder = BRepBuilderAPI_MakeFace(profile6_wire)
profile6_face_builder.Build()
if not profile6_face_builder.IsDone():
    raise RuntimeError("Profile 6 face construction failed.")
profile6_face = profile6_face_builder.Face()

# Extrude from Y=-16.5 to Y=-10.5 (distance = 6.0 in +Y direction)
extrude_vec6 = gp_Vec(0, 6.0, 0)
prism6 = BRepPrimAPI_MakePrism(profile6_face, extrude_vec6)
prism6.Build()
if not prism6.IsDone():
    raise RuntimeError("Profile 6 extrusion failed.")
extruded_profile6 = Solid(prism6.Shape())
print("  Extruded profile 6 created.")

# ═══════════════════════════════════════════════════════════════════════════════
# EXTRUDED PROFILE 7 (joined with extruded_profile6)
# ═══════════════════════════════════════════════════════════════════════════════
print("  Building extruded profile 7...")

profile7_pts = [
    Vector(29.0, -16.5, 22.0),
    Vector(28.0, -14.5, 24.0),
    Vector(28.0, -12.5, 24.0),
    Vector(29.0, -10.5, 22.0),
]

# Build closed wire
profile7_wire_builder = BRepBuilderAPI_MakeWire()
for i in range(len(profile7_pts)):
    edge = Edge.make_line(profile7_pts[i], profile7_pts[(i + 1) % len(profile7_pts)])
    profile7_wire_builder.Add(edge.wrapped)
profile7_wire_builder.Build()
if not profile7_wire_builder.IsDone():
    raise RuntimeError("Profile 7 wire construction failed.")
profile7_wire = profile7_wire_builder.Wire()

# All points lie on the plane defined by X=29 to X=28, need to find the plane
# The extrude target is (27.0, -14.5, 24.0), profile points have X from 28 to 29
# Extrude in -X direction: from X≈29 toward X=27, distance = 2.0
# But the profile is not planar in X — points are at X=29 and X=28
# Use BRepFill_Filling for non-planar face

cut_filler7 = BRepFill_Filling()
edges7 = []
for i in range(len(profile7_pts)):
    edge = Edge.make_line(profile7_pts[i], profile7_pts[(i + 1) % len(profile7_pts)])
    edges7.append(edge)
for edge in edges7:
    cut_filler7.Add(edge.wrapped, GeomAbs_C0)
cut_filler7.Build()
if not cut_filler7.IsDone():
    raise RuntimeError("Profile 7 face filling failed.")
profile7_face = cut_filler7.Face()

# Extrude in -X direction: target X=27.0, min profile X=28.0, so distance = -1.0
# But we need to cover from max X=29 to X=27, so extrude -2.0 in X
extrude_vec7 = gp_Vec(-2.0, 0, 0)
prism7 = BRepPrimAPI_MakePrism(profile7_face, extrude_vec7)
prism7.Build()
if not prism7.IsDone():
    raise RuntimeError("Profile 7 extrusion failed.")
extruded_profile7 = Solid(prism7.Shape())
print("  Extruded profile 7 created.")

# Fuse with extruded_profile6
fuse_p6_p7 = BRepAlgoAPI_Fuse(extruded_profile6.wrapped, extruded_profile7.wrapped)
fuse_p6_p7.Build()
if not fuse_p6_p7.IsDone():
    raise RuntimeError("Fuse of profile 6 and 7 failed.")
extruded_profile6 = Solid(fuse_p6_p7.Shape())
print("  Extruded profile 7 fused with extruded profile 6.")

# ═══════════════════════════════════════════════════════════════════════════════
# LOFT CUT + EXTRUDE CUT ON EXTRUDED PROFILE 6
# ═══════════════════════════════════════════════════════════════════════════════
print("  Building loft cut and extrude cut on extruded profile 6...")

# Circle 1: centre (29.0, -13.5, 21.0), dia 3.84mm
cut_c1_centre = gp_Pnt(29.0, -13.5, 21.0)
cut_c1_radius = 3.84 / 2.0

# Circle 2: centre (27.667, -13.5, 21.0), dia 1.92mm
cut_c2_centre = gp_Pnt(27.667, -13.5, 21.0)
cut_c2_radius = 1.92 / 2.0

# Both circles in YZ plane, normal along X axis
cut_circ1_ax = gp_Ax2(cut_c1_centre, gp_Dir(-1, 0, 0))
gp_cut_circ1 = gp_Circ(cut_circ1_ax, cut_c1_radius)
cut_circ1_edge = BRepBuilderAPI_MakeEdge(gp_cut_circ1).Edge()
cut_circ1_wire_b = BRepBuilderAPI_MakeWire()
cut_circ1_wire_b.Add(cut_circ1_edge)
cut_circ1_wire_b.Build()
if not cut_circ1_wire_b.IsDone():
    raise RuntimeError("Cut circle 1 wire failed.")

cut_circ2_ax = gp_Ax2(cut_c2_centre, gp_Dir(-1, 0, 0))
gp_cut_circ2 = gp_Circ(cut_circ2_ax, cut_c2_radius)
cut_circ2_edge = BRepBuilderAPI_MakeEdge(gp_cut_circ2).Edge()
cut_circ2_wire_b = BRepBuilderAPI_MakeWire()
cut_circ2_wire_b.Add(cut_circ2_edge)
cut_circ2_wire_b.Build()
if not cut_circ2_wire_b.IsDone():
    raise RuntimeError("Cut circle 2 wire failed.")

# Loft between the two circles
cut_circ_loft = BRepOffsetAPI_ThruSections(True)
cut_circ_loft.AddWire(cut_circ1_wire_b.Wire())
cut_circ_loft.AddWire(cut_circ2_wire_b.Wire())
cut_circ_loft.Build()
if not cut_circ_loft.IsDone():
    raise RuntimeError("Circle loft cut failed.")
circ_loft_tool = cut_circ_loft.Shape()
print("    Loft cut tool created.")

# Extrude circle 1 (larger) in +X to cut through profile6 on that side
cut_circ1_face_b = BRepBuilderAPI_MakeFace(cut_circ1_wire_b.Wire())
cut_circ1_face_b.Build()
ext_plus_x = BRepPrimAPI_MakePrism(cut_circ1_face_b.Face(), gp_Vec(5.0, 0, 0))
ext_plus_x.Build()
if not ext_plus_x.IsDone():
    raise RuntimeError("Circle 1 +X extrusion failed.")

# Extrude circle 2 (smaller) in -X from X=27.667 to X=27.0
cut_circ2_face_b = BRepBuilderAPI_MakeFace(cut_circ2_wire_b.Wire())
cut_circ2_face_b.Build()
ext_minus_x = BRepPrimAPI_MakePrism(cut_circ2_face_b.Face(), gp_Vec(-0.667, 0, 0))
ext_minus_x.Build()
if not ext_minus_x.IsDone():
    raise RuntimeError("Circle 2 -X extrusion failed.")

# Fuse all three cut tools into one
fuse_cut1 = BRepAlgoAPI_Fuse(circ_loft_tool, ext_plus_x.Shape())
fuse_cut1.Build()
if not fuse_cut1.IsDone():
    raise RuntimeError("Fuse cut tools 1 failed.")

fuse_cut2 = BRepAlgoAPI_Fuse(fuse_cut1.Shape(), ext_minus_x.Shape())
fuse_cut2.Build()
if not fuse_cut2.IsDone():
    raise RuntimeError("Fuse cut tools 2 failed.")
combined_cut_tool = fuse_cut2.Shape()
print("    Combined cut tool created.")

# Cut from extruded_profile6 (which already includes profile7)
cut_op_final = BRepAlgoAPI_Cut(extruded_profile6.wrapped, combined_cut_tool)
cut_op_final.Build()
if not cut_op_final.IsDone():
    raise RuntimeError("Final cut on profile 6 failed.")
extruded_profile6 = Solid(cut_op_final.Shape())
print("    Cut applied to extruded profile 6 (both bodies).")

# ═══════════════════════════════════════════════════════════════════════════════
# MIRROR EXTRUDED PROFILE 6 ABOUT YZ PLANE (X=0)
# ═══════════════════════════════════════════════════════════════════════════════
print("  Mirroring extruded profile 6...")

mirror_trsf = gp_Trsf()
mirror_trsf.SetMirror(gp_Ax2(gp_Pnt(0, 0, 0), gp_Dir(1, 0, 0)))

# Create the transformed shape
mb_p6 = BRepBuilderAPI_Transform(extruded_profile6.wrapped, mirror_trsf, True)
mb_p6.Build()

# REMOVED: .Complement() - This was likely causing the part to "disappear" or invert
mirrored_shape = mb_p6.Shape()

# Wrap it as a Solid (or Compound if the original was complex)
extruded_profile6_mirror = Solid(mirrored_shape)
print("  Extruded profile 6 mirrored.")

