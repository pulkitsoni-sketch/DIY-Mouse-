# ── Helper ────────────────────────────────────────────────────────────────────
def make_solid_from_splines(pts_list, label="surface", flip=False):
    print(f"  Building {label}...")
    edges = [Edge.make_spline([Vector(*p) for p in pts]) for pts in pts_list]

    filler = BRepFill_Filling()
    for edge in edges:
        filler.Add(edge.wrapped, GeomAbs_C0)
    filler.Build()
    if not filler.IsDone():
        raise RuntimeError(f"BRepFill_Filling failed for {label}.")

    face = Face(filler.Face())

    centre = face.center()
    normal = face.normal_at(centre)
    normal_vec = gp_Vec(normal.X, normal.Y, normal.Z)

    should_reverse = normal_vec.Dot(REFERENCE_DIR) < 0
    if flip:
        should_reverse = not should_reverse
    if should_reverse:
        face = Face(face.wrapped.Reversed())

    offset = BRepOffset_MakeOffset()
    offset.Initialize(
        face.wrapped, THICKNESS, 1e-3,
        BRepOffset_Skin, False, False, GeomAbs_Arc, True,
    )
    offset.MakeOffsetShape()
    if not offset.IsDone():
        raise RuntimeError(f"BRepOffset_MakeOffset failed for {label}.")
    return Solid(offset.Shape())


def fuse_solids(solids):
    """Fuse a list of solids sequentially using BRepAlgoAPI_Fuse."""
    result = solids[0].wrapped
    for s in solids[1:]:
        f = BRepAlgoAPI_Fuse(result, s.wrapped)
        f.Build()
        if not f.IsDone():
            raise RuntimeError("Fuse failed.")
        result = f.Shape()
    return result


